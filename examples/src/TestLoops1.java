public class TestLoops1 {
    public static void main(String[] args){
        //Loop - doing something again and agian

        //1. while

        int c=0;

        while (c<=5) {
            //System.out.println("Hello World!");
            c++;
        }

        //2. do-while Loop
        int t =0;
        do{
            //System.out.println("Hello World!");
            t++;
        }while (t>=5);

        //3. for Loop

        for (int i=0;i<=5;i++){
            System.out.println("Hello World!");
        }

    }
}
